    public function store(StorePostRequest $request)
    {
        dd($request);

        if (Auth::user()->id != 1) {
            return redirect('/');
        }

        $post = new Post;

        $post->user_id = Auth::user()->id;

        $post->title = $request->title;
        $post->slug = Str::slug($request->title, "-");

        $post->content = $request->content;
        $post->category_id = 1;

        $post->save();

        return redirect('/');

    }

